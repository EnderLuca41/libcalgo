static uint32_t h_byte(uint8_t x, const uint32_t *l, uint8_t i, uint8_t k){
    if(k == 4){
        if(i == 0 || i == 3) x = Q1[x];
        else x = Q0[x];

        x ^= (l[3] >> (i - 3));
    }

    if(k >= 3){
        if(i == 0 || i == 1) x = Q1[x];
        else x = Q0[x];

        x ^= (l[2] >> (i - 3));
    }

    if(i == 1 || i == 3) x = Q1[x];
    else x = Q0[x];

    x ^= (uint8_t) (l[1] >> (i * 8));

    if(i == 2 || i == 3) x = Q1[x];
    else x = Q0[x];

    x ^= (uint8_t) (l[0] >> (i * 8));

    if(i == 0 || i == 2) x = Q1[x];
    else x = Q0[x];

    uint32_t result = 0;
    uint8_t *resultArr = (uint8_t *) &result;

    for(unsigned int j = 0; j < 4; j++){
        resultArr[j] = galois_mult(MDS[j][i], x, Wmds);
    }

    return result;
}

static uint32_t h(uint8_t x, uint8_t k, uint32_t l[]){

    uint32_t result = 0;
    for(unsigned int i = 0; i < 4; i++){
        result ^= h_byte(x, l, i, k);
    }
    return result;
}

void twofish_init(const uint8_t *key, uint8_t keyLen, TwofishContext *ctx){
    if(keyLen % 8 != 0 || keyLen > 32){
        return;
    }

    uint8_t k = keyLen / 8;

    //Calculation of first two vectors
    uint32_t me[4];
    uint32_t mo[4];


    for(unsigned int i = 0; i < k; i++){
        memcpy(me + i, &key[i * 8], sizeof(uint32_t));
        memcpy(mo + i, &key[i * 8 + 4], sizeof(uint32_t));
    }


    //Calculation of third vector
    uint32_t s[4];
    uint32_t buffer[2];
    for(unsigned int i = 0; i < k; i++){
        memcpy(buffer, key + i * 8, sizeof(buffer));
        rs_matrix_mult((uint8_t *)buffer, (uint8_t *) (s + i));
    }

    //Reverse s
    if(k == 2){
        uint32_t buff = s[0];
        s[0] = s[1];
        s[1] = buff;
    }
    else if(k == 3){
        uint32_t buff = s[0];
        s[0] = s[2];
        s[2] = buff;
    }
    else{
        uint32_t buff = s[0];
        s[0] = s[3];
        s[3] = buff;

        buff = s[1];
        s[1] = s[2];
        s[2] = buff;
    }

    //Calculate subkeys
    for(uint8_t i = 0; i < 20; i++){
        uint32_t a = h(2 * i, k, me);
        uint32_t b = left_rot32(h(2 * i + 1, k, mo), 8);
        a += b;
        ctx->subkeys[i * 2] = a;
        a += b;
        ctx->subkeys[i * 2 + 1] = left_rot32(a, 9); 
    }

    //Calculate key dependent S-Boxes
    for(unsigned int i = 0; i < 256; i++){
        ctx->s0[i] = h_byte(i, s, 0, k);
        ctx->s1[i] = h_byte(i, s, 1, k);
        ctx->s2[i] = h_byte(i, s, 2, k);
        ctx->s3[i] = h_byte(i, s, 3, k);
    }
}