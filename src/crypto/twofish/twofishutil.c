//Reducing polynomial of finite field GF(2^8) with RS
const uint16_t Wrs = 0b0000000001001101;

//Reducing polynomial of finite field GF(2^8) with MDS
const uint16_t Wmds = 0b0000000001101001;


static uint8_t galois_mult(uint8_t a, uint8_t b, uint8_t w){
    uint8_t result = 0;
    for(unsigned int i = 0; i < 8; i++) {
        if((a & 0x01) != 0){
            result ^= b;
        }
        
        if((b & 0x80)){
            b = (b << 1) ^ w;
        } 
        else{
            b <<= 1;
        }
        a >>= 1;
    }

    return result;
}

